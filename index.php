            $(block[i]).addClass('animated slideOutRight').removeClass('slideInLeft').removeClass('slideInRight');
            $(block[j]).addClass('animated slideInLeft').removeClass('slideOutRight').removeClass('slideOutLeft').show();

            i--;
        }

        block.each(function(i) {
            if(i != 0)
                $(this).hide();
        });

        var autoSlide = setInterval(functionNext, time);

        // Acciones Flechas Slider

        $(document).on('click', 'a.next', function() {
            //alert("Siguiente!");
