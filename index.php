    $(document).on('ready', function(){
        var block = $('.divItems');
        var blockSize = block.length;
        var i = 0, j;

        function functionNext() {
            if (i < 0) {
                i = blockSize - 1;
            }
            if (i >= blockSize) {
                i = 0;
            }

            j = i + 1;
            if (j >= blockSize) {
                j = 0;
            }


            if(i<j) {
                $(block[i]).attr('style', 'position:absolute;');
                $(block[j]).attr('style', 'position:relative;');
            }
            $(block[i]).addClass('animated slideOutLeft').removeClass('slideInRight').removeClass('slideInLeft');
            $(block[j]).addClass('animated slideInRight').removeClass('slideOutLeft').removeClass('slideOutRight').show();

            i++;
        }

        function functionPrev() {
            if (i >= blockSize) {
                i = 0;
            }
            if (i < 0) {
                i = blockSize - 1;
            }

            j = i - 1;
            if (j < 0) {
                j = blockSize - 1;
            }


            if(i<j) {
                $(block[i]).attr('style', 'position:absolute;');
                $(block[j]).attr('style', 'position:relative;');
