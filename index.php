            clearInterval(autoSlide);
            autoSlide = setInterval(functionNext, time);
        });
        $(document).on('click', 'a.prev', function() {
            //alert("Anterior!");
            functionPrev();
            clearInterval(autoSlide);
            autoSlide = setInterval(functionNext, time);
